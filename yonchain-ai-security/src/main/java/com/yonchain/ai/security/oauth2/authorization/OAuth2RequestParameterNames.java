package com.yonchain.ai.security.oauth2.authorization;

/**
 * OAuth2 请求参数名称常量类
 *
 * @author Cgy
 * @since 1.0.0
 */
public final class OAuth2RequestParameterNames {

    /**
     * {@code username} - used in Access Token Request.
     */
    public static final String USERNAME = "username";

    /**
     * {@code password} - used in Access Token Request.
     */
    public static final String PASSWORD = "password";

    /**
     * {@code email} - used in Access Token Request.
     */
    public static final String EMAIL = "email";

    /**
     * {@code token} - used in Access Token Request.
     */
    public static final String TOKEN = "token";

}
